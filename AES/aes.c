/*
  gf8_mul 함수는 입력된 8비트 정수 a와 b를 gf(2^8) 상에서 곱해준 후 그 결과값을 반환한다.
  매 루프마다 b의 각자리 비트값에 따라 a의 값을 결과값 r에 더해주며(XOR), 루프를 돌때마다
  XTIME 매크로를 통해 a의 x 지수값을 하나씩 올려준다.
*/
static uint8_t gf8_mul(uint8_t a, uint8_t b)
{
    uint8_t r = 0x0;
    while(b > 0) {
        if (b & 0x1) r = r ^ a;
        b = b >> 1;
        a = XTIME(a);
    }
    return r;
}

/*
  AddRoundKey는 입력된 8비트 정수 포인터 state의 성분에 32비트 정수 포인터 roundKey의 성분을 XOR하여 반환한다.
  이때 전달받은 state의 word가 가로성분(column) 단위로 끊어지므로 Nb 길이의 루프를 돌린다.
  (uint32_t *)state[i] ^ roundKey[i] 의 양상으로 XOR 작업이 수행된다.
*/
static void AddRoundKey(uint8_t *state, const uint32_t *roundKey) {
  int i;
  for (i = 0; Nb > i; i++)
    *(((uint32_t *)state) + i) = *(((uint32_t *)state) + i) ^ roundKey[i];
}

/*
  SubBytes는 입력된 state의 성분을 인덱스로 sbox 혹은 isbox를 참조하여 그 참조값을 원래 state의 성분으로 대체한다.
  이때 mode(암호:1, 복호:0) 값에 따라 참조하는 배열이 달라진다.
*/
static void SubBytes(uint8_t *state, int mode) {
  int i;
  for(i = 0; BLOCKLEN > i; i++)
    *(state + i) = (mode ? sbox[*(state + i)] : isbox[*(state + i)]);
}

/*
  ShiftRows는 입력된 state의 배열 구조를 재조합한다. 재조합 작업은 첫 세로성분(state[0, 4, 8, 12])을
  제외하고 이루어진다. 재조합 작업을 수행하기 위해 state의 성분들을 임시로 저장할 배열 tempVect를 선언한다.
  state의 인덱스가 순차적으로 0부터 15까지 증가한다고 가정했을때 표준스펙에 정의된 재조합 작업은
  *(tmpVect + ((((i / Nb) * Nb) + ((mode ? 5 : 13) * (i % Nb))) % BLOCKLEN))로 일반화 될 수 있다.
  mode 따라 재조합 결과물은 달라진다.
*/
static void ShiftRows(uint8_t *state, int mode) {
  int i;
  uint8_t tempVect[BLOCKLEN] = {};
  memcpy(tempVect, state, BLOCKLEN);
  for (i = 0; BLOCKLEN > i; i++)
    *(state + i) = *(tempVect + ((((i / Nb) * Nb) + ((mode ? 5 : 13) * (i % Nb))) % BLOCKLEN));
}

/*
  MixColumns는 입력된 state 배열에 주어진 상수배열 M/IM과 gf(2^8)상의 행렬곱을 한다.
  행렬곱 작업을 수행하기 위해 state의 성분들을 임시로 저장할 배열 tempVect를 선언한다.
  gf(2^8) 상의 곱을 구현하기 위해 gf8_mul 함수를 활용한다.
*/
static void MixColumns(uint8_t *state, int mode) {
  int i;
  uint8_t tempVect[BLOCKLEN] = {};
  memcpy(tempVect, state, BLOCKLEN);
  for (i = 0; BLOCKLEN > i; i++) {
    *(state + i) = gf8_mul(*(tempVect + ((i / Nb) * Nb)), (mode ? M[(Nb * (i % Nb))] : IM[(Nb * (i % Nb))])) ^
                    gf8_mul(*(tempVect + ((i / Nb) * Nb) + 1), (mode ? M[(Nb * (i % Nb)) + 1] : IM[(Nb * (i % Nb)) + 1])) ^
                    gf8_mul(*(tempVect + ((i / Nb) * Nb) + 2), (mode ? M[(Nb * (i % Nb)) + 2] : IM[(Nb * (i % Nb)) + 2])) ^
                    gf8_mul(*(tempVect + ((i / Nb) * Nb) + 3), (mode ? M[(Nb * (i % Nb)) + 3] : IM[(Nb * (i % Nb)) + 3]));
  }
}

/*
 * Generate an AES key schedule
 * 제작자 주석: KeyExpansion은 주어진 Nk 워드 배열 key를 표준스펙에 정의된 방식에 따라 (Nb*(Nr+1)) 워드 배열 roundKey로 확장한다.
 *              처음 Nk 길이의 루프동안 key의 Nk개 워드를 roundKey에 복사하는 동시에, 키 확장에 사용될 gVect 값을 계산한다.
 *              이후 RNDKEYSIZE - Nk 길이의 루프동안 개별 32비트 워드 roundKey 성분을 계산하는데, 이때 인덱스가 Nk로 나누어질 때마다
 *              바로 직전의 워드와 XOR를 할지 gVect와 XOR를 할지가 달라진다.
 *              루프가 Nk번 만큼 돌면 다시 gVect를 계산한다. 표준스펙에 정의된 'Sbox(LRotWord(w)) XOR Rcon'은 다음과 같은 c구문으로 표현된다.
 *              sbox[((uint8_t *)(roundKey + i))[(j + 1) % Nk]] ^ (j == 0 ? Rcon[(i / Nk) + 1] : 0)
 */
void KeyExpansion(const uint8_t *key, uint32_t *roundKey)
{
  int i, j;
  uint8_t gVect[Nk] = {};
  for(i = 0; Nk > i; i++) {
    memcpy(roundKey + i, key + (Nk * i), Nk);
    gVect[i] = sbox[*(key + Nk * (Nk - 1) + ((i + 1) % Nk))] ^ (i == 0 ? Rcon[1] : 0);
  }
  for(i = Nk; RNDKEYSIZE > i; i ++) {
    *(roundKey + i) = *(roundKey + i - Nk) ^ (i % Nk == 0 ? *((uint32_t *)gVect) : *(roundKey + i - 1));
    if (i % Nk == (Nk - 1))
      for (j = 0; Nk > j; j++)
        gVect[j] = sbox[((uint8_t *)(roundKey + i))[(j + 1) % Nk]] ^ (j == 0 ? Rcon[(i / Nk) + 1] : 0);
  }
}

/*
 * AES cipher function
 * If mode is nonzero, then do encryption, otherwise do decryption.
 * 제작자 주석: Cipher는 AES 알고리즘의 메인 구현체이다. 표준 스펙에 정의된 순서대로 앞서 구현된
 *              static 함수들 (AddRoundKey, SubBytes, ShiftRows, MixColumns)이 호출되어 state를 조작한다.
 *              mode값에 따라 state에 행해지는 실제 작업이 달라진다.
 *              mode = 0일 때, 즉 복호화시에, MixColumns를 동반한 AddRoundKey가 실행될 경우 해당 AddRoundKey에
 *              전달될 roundKey 배열에 mode = 0의 MixColumns 작업을 수행한다. roundKey의 값을 보존하기 위해
 *              해당 작업은 tmp 배열에 각 라운드에 맞는 roundKey 부분을 임시로 복사하여 수행한다.
 */
void Cipher(uint8_t *state, const uint32_t *roundKey, int mode)
{
  int i;
  uint8_t tmp[BLOCKLEN] = {};
  AddRoundKey(state, (mode ? roundKey : (roundKey + Nr * Nk)));
    for (i = 1; Nr > i; i++) {
      SubBytes(state, mode);
      ShiftRows(state, mode);
      MixColumns(state, mode);
      if (!mode) {
        memcpy(tmp, (roundKey + Nr * Nk - i * Nk), BLOCKLEN);
        MixColumns(tmp, mode);
      }
      AddRoundKey(state, (mode ? roundKey + i * Nk : (uint32_t *)tmp));
  }
  SubBytes(state, mode);
  ShiftRows(state, mode);
  AddRoundKey(state, (mode ? roundKey + Nr * Nk : roundKey));
}
